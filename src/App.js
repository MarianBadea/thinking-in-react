function filterByName (products, filterText) {
  let how = (product) => {
    return product.name.includes(filterText);
  }

  return products.filter ( how )
}

function filterByStock (products) {
  let how = (product) => {
    return product.stocked;
  }

  return products.filter ( how );
}


